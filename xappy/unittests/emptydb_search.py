from xappytest import *

class TestEmptyDbSearch(TestCase):
    def pre_test(self):
        self.indexpath = os.path.join(self.tempdir, 'foo')
        iconn = xappy.IndexerConnection(self.indexpath)
        iconn.close()
        self.sconn = xappy.SearchConnection(self.indexpath)

    def post_test(self):
        self.sconn.close()

    def test_eterms(self):
        """Check that a similarity search on an empty database works.
        
        This is a regression test - this used to fail due to a missing
        _next_docid member of SearchConnection in empty databases.

        """
        self.sconn.query_similar('1', deny=['supplier'])

if __name__ == '__main__':
    main()
